package java_training_;

public class SwitchStatements {

	public static void main(String[] args) {
		int value = 2;
		
		switch(value)
		{
		case 1:{System.out.println("case 1 got match");
		break;}
		case 2:{System.out.println("case 2 got match");
		break;}
		case 3:{System.out.println("case 3 got match");
		break;}
		default:{System.out.println("I'm default");}
		}
	}

}
